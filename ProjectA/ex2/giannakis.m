
%Παίρνει σαν ορίσματα τους σωρείτες 3ης τάξης και την τάξη του ΜΑ μοντέλου
%Επιστρέφει την προσσεγιστική απόκριση του συστήματος χρησιμοποιώντας τον
%τύπο του Γιαννάκη

function  h = giannakis(c3, q)   
  
h=zeros(q+1,1);
 
  for k=0:q
 h(k+1) = (c3(q+21,k+21) )/ c3(q+21,21);
  end
  
end
